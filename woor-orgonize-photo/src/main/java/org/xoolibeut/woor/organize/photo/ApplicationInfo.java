package org.xoolibeut.woor.organize.photo;

public class ApplicationInfo {
	private boolean viewLogConsole;
	private String startDate;
	private String applicationLog;

	public boolean isViewLogConsole() {
		return viewLogConsole;
	}

	public void setViewLogConsole(boolean viewLogConsole) {
		this.viewLogConsole = viewLogConsole;
	}

	public String getStartDate() {
		return startDate;
	}

	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}

	public String getApplicationLog() {
		return applicationLog;
	}

	public void setApplicationLog(String applicationLog) {
		this.applicationLog = applicationLog;
	}
}
